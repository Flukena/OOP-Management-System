    public void load() {
        try {
            //แสดงผลบน JTable
            pst = con.prepareStatement("select * from product");
            ResultSet result = pst.executeQuery();

            ResultSetMetaData rd = result.getMetaData();

            int a = rd.getColumnCount();

            def = (DefaultTableModel) product.getModel();
            def.setRowCount(0);

            while (result.next()) {
                Vector vec = new Vector();
                for (int i = 1; i <= a; i++) {
                    vec.add(result.getString("num"));
                    vec.add(result.getString("id"));
                    vec.add(result.getString("pro"));
                    vec.add(result.getString("cost"));
                    vec.add(result.getString("quan"));
                }
                def.addRow(vec);//เพิ่มข้อฒูลลงบน Jtable ROW
            }

        } catch (SQLException ex) {
            Logger.getLogger(product.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


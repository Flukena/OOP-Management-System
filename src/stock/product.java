    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        //ปุ่มลบข้อมูล
        def = (DefaultTableModel) product.getModel();
        int selector = product.getSelectedRow();
        int num = Integer.parseInt(def.getValueAt(selector, 0).toString());//เลือกข้อมูลจากลำดับของสินค้า

        try {

            pst = con.prepareStatement("delete from product where num = ?");
            pst.setInt(1, num);
            pst.executeUpdate();//Update to Database

            JOptionPane.showMessageDialog(this, "ลบข้อมูลสำเร็จ");

            id.setText("");
            name.setText("");
            cost.setText("");
            quan.setText("");

            id.requestFocus();

            load();
        } catch (SQLException ex) {
            Logger.getLogger(product.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteActionPerformed

